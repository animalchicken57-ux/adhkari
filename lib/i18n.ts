export const dict: Record<Lang, Dict> = { ar, en };

export type TFunc = (key: string, vars?: Record<string, string | number>) => string;

export function getT(lang: Lang): TFunc {
  const d = dict[lang] || dict.ar;
  return (key, vars) => {
    let s = d[key] ?? dict.ar[key] ?? key;
    if (vars) {
      for (const k of Object.keys(vars)) s = s.replaceAll(`{${k}}`, String(vars[k]));
    }
    return s;
  };
}
